            <div>
                <h2>Physiologie et écologie</h2>
            </div>
            <div>
                <p>
                    Les crabes sont des espèces de cétacés décapodes principalement de nature marines,
                    même si certaines espèces vivent dans des eaux douces; Ils disposent de 5 paires de pattes (ou péréiopodes) dont la première forment une paire de pinces.
                    Il y aurait plus de 76 espèces de crabes dans le monde.  
                </p>
            </div>
        </div>
        <div>
            <div>
                <h2>Pêche et consommation</h2>
            </div>
            <div>
                <p>
                    On estime la pêche de crabe d'atteindre les 1,2 millions de tonnes par an (donnée récoltée en 2003).
                    Autant il faut noter que le crabe est un animal cotier très facile à capturer, et donc ce chiffre ne contient,
                    ni la pêche artisanale, ni la pêche individuelle.
                </p>
