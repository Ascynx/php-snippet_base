                <h2>Symbolisme en Europe</h2>
            </div>
            <div>
                <p>
                    Alors que durant l'antiquité, le crabe est considéré comme animal protecteur,
                    quand nous entrons le Moyen Age, sa réputation change comme le reste des animaux marins marcheurs, il devient un symbole maléfique;
                    Ce qui est accentué par ses habitudes nécrophages.
                </p>
            </div>
        </div>
        <div>
            <div>
                <h2>Terminologie</h2>
            </div>
            <div>
                <p>
                    Le mot crabe, n. féminine jusqu'au 17ème, aura emprunté deux voies différentes: d'une part, du néerlandais, crabbe au masculin,
                    via le picard, crabo, crape et finalement, le wallon, crâbe; d'autre part, du vieux norrois, krabbi, au masculin, via le normand crabe,
                    crabbe au féminin. Crabe a finalement remplacé le mot chancre, du latin cancer.
                </p>
