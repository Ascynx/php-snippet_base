
    <div>
        <h3>Bateaux à voile</h3>
        <ul>
            <li>
                <strong>Voilier</strong> : Utilisé pour la navigation de plaisance ou la compétition, il se déplace principalement grâce à la force du vent sur ses voiles.
            </li>
            <li>
                <strong>Catamaran</strong> : Bateau à deux coques, réputé pour sa stabilité et sa vitesse, souvent utilisé pour les croisières ou les sports nautiques.
            </li>
            <li>
                <strong>Goélette</strong> : Grand voilier traditionnel doté de plusieurs mâts, utilisé historiquement pour le transport de marchandises ou la pêche.
            </li>
        </ul>
    </div>