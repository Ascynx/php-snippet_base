<div>
        <h3>Bateaux à moteur</h3>
        <ul>
            <li>
                <strong>Vedette</strong> : Bateau rapide à moteur, idéal pour les promenades, la pêche ou le transport de passagers sur de courtes distances.
            </li>
            <li>
                <strong>Péniche</strong> : Long bateau à fond plat, principalement utilisé pour le transport de marchandises sur les rivières et canaux.
            </li>
            <li>
                <strong>Yacht</strong> : Bateau de luxe à moteur, conçu pour la plaisance, souvent équipé de cabines confortables et de nombreux équipements.
            </li>
        </ul>
    </div>